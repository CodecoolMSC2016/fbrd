class Person:
    def __init__(self, first_name, last_name, year_of_birth, gender, energy_level = 75,
                 social_life = 20, darts_skill_level = 0, playstation_skill_level = 0, hunger_level = 0, blood_alcohol_level = 0):
        self.first_name = first_name
        self.last_name = last_name
        self.year_of_birth = year_of_birth
        self.gender = gender
        self.energy_level = energy_level
        self.social_life = social_life
        self.darts_skill_level = darts_skill_level
        self.playstation_skill_level = playstation_skill_level
        self.hunger_level = hunger_level
        self.blood_alcohol_level = blood_alcohol_level